uint8_t getFontWidth() {
    return font_width;
}

uint8_t getFontHeight() {
    return font_height;
}

uint8_t * getFontChar(char c) {
    if (c < min_char || c > max_char) {
        return NULL;
    }
    return font_bitmap + (c - min_char) * font_height;
}